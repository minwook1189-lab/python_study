# Day 07: 딕셔너리 기초
# 데이터 분석의 기본! key-value 구조 배우기

# 1. 딕셔너리 만들기
성적표 = {
    "철수": 85,
    "영희": 92,
    "민수": 78,
    "지연": 95
}

# 2. 전체 출력하기
print("=== 성적표 ===")
for 이름, 점수 in 성적표.items():
    print(f"{이름}: {점수}점")

print(성적표["철수"])

# 3. 기본 통계
print(f"\n총 학생 수: {len(성적표)}명")
print(f"평균 점수: {sum(성적표.values()) / len(성적표)}점")
print(f"최고 점수: {max(성적표.values())}점")
print(f"최저 점수: {min(성적표.values())}점")

# 4. 특정 학생 조회
print("\n=== 점수 조회 ===")
찾을학생 = input("조회할 학생 이름: ")

if 찾을학생 in 성적표:
    print(f"{찾을학생}의 점수는 {성적표[찾을학생]}점입니다.")
else:
    print("해당 학생이 없습니다.")

# 5. 새 학생 추가
print("\n=== 학생 추가 ===")
새이름 = input("추가할 학생 이름: ")
새점수 = int(input("점수: "))
성적표[새이름] = 새점수

print("\n=== 업데이트된 성적표 ===")
for 이름, 점수 in 성적표.items():
    print(f"{이름}: {점수}점")
